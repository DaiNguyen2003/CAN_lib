void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    CAN_RxHeaderTypeDef RxHeader;
    uint8_t RxData[8];

    if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData) == HAL_OK)
    {
        switch (CurrentCarProfile)
        {
            case CAR_VF_LIMO:
                CAN_Decode_Message(RxHeader.StdId, RxData, VF_LIMO_Msg, SIZE_VF_LIMO_Msg, &VF_LIMO_Data);
                /* --- LIMO Logic --- */
                if (VF_LIMO_Data.CreepMode.Disable_Sts == 0) {
                     // Creep ON
                }
                break;

            case CAR_VF789:
                CAN_Decode_Message(RxHeader.StdId, RxData, VF789_Msg, SIZE_VF789_Msg, &VF789_Data);
                /* --- VF789 Logic --- */
                if (VF789_Data.CreepMode.Mode_Req == 1) {
                     // Creep ON
                }
                break;

            default:
                break;
        }
    }
}
